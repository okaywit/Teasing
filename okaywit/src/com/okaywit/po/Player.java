package com.okaywit.po;

public class Player {
        private int x;
        private int y;
        private int direction;

        public int getX() {
                return x;
        }

        public void setX(int x) {
                this.x = x;
        }

        public int getY() {
                return y;
        }

        public void setY(int y) {
                this.y = y;
        }

        public int getDirection() {
                return direction;
        }

        public void setDirection(int direction) {
                this.direction = direction;
        }

}
